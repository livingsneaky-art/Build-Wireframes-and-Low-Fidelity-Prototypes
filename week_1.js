//Optional - Learn more about how research inform ideation
/*
    - Empathy maps = explore users’ four main motivations: what the user says, thinks, does, and feels. The insights gathered from 
        empathy maps help you come up with ideas for solutions that address the user’s real problems. 

    - Personas = place the users who you’re designing for front-and-center. By creating detailed user profiles, you can clearly 
        envision potential users that you’d design for.

    - User stories = determine which user needs are the most critical to address with your designs. This direction will help focus your ideation.

    - User journeys = help you come up with ideas for designs that truly support the users’ needs and solve their problems. 

    - Problem statement = is a clear description of the user’s need that should be addressed. The problem statement you created in 
        the last course will guide the focus of your ideation. 
*/

